print("-------Ejemplo de división de entero y decimal--------")
num= int(input("Introduce primer número: "))
num2= float(input("Introduce segundo número: "))
resultado=num/num2
cociente=num//num2
resto=num%num2
print("Resultado: ",resultado)
print("División entera, nos da el cociente entero o MOD: ",cociente)
print("El resto es:",resto)
print ("Si dividimos entero y decimal, el resultado siempre es decimal")
